// ═══════════════════════════════════════════════════════════
// MATCHES
// ═══════════════════════════════════════════════════════════

export const matches = {
  async list(eventId, filters = {}) {
    let query = supabase
      .from("matches")
      .select(
        "*, team_a:teams!matches_team_a_id_fkey(id, name), team_b:teams!matches_team_b_id_fkey(id, name), playing_area:playing_areas(number, name), pool:pools(name), bracket:brackets(name)"
      )
      .eq("event_id", eventId)
      .order("round")
      .order("match_number");

    if (filters.status) query = query.eq("status", filters.status);
    if (filters.poolId) query = query.eq("pool_id", filters.poolId);
    if (filters.bracketId) query = query.eq("bracket_id", filters.bracketId);

    const { data, error } = await query;
    if (error) throw error;
    return data;
  },

  async getLive(eventId) {
    const { data, error } = await supabase
      .from("matches")
      .select(
        "*, team_a:teams!matches_team_a_id_fkey(id, name), team_b:teams!matches_team_b_id_fkey(id, name), playing_area:playing_areas(number, name)"
      )
      .eq("event_id", eventId)
      .in("status", ["live", "score_entered", "disputed"])
      .order("playing_area_id");
    if (error) throw error;
    return data;
  },

  // Captain: submit score (home captain only)
  async submitScore(matchId, teamAScore, teamBScore, captainPhone) {
    const { data, error } = await supabase
      .from("matches")
      .update({
        team_a_score: teamAScore,
        team_b_score: teamBScore,
        score_entered_at: new Date().toISOString(),
        score_entered_by: captainPhone,
        status: "score_entered",
      })
      .eq("id", matchId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Captain: verify score (away captain)
  async verifyScore(matchId) {
    const { data: match } = await supabase
      .from("matches")
      .select("team_a_score, team_b_score, team_a_id, team_b_id")
      .eq("id", matchId)
      .single();

    const winnerId =
      match.team_a_score > match.team_b_score ? match.team_a_id : match.team_b_id;
    const loserId =
      match.team_a_score > match.team_b_score ? match.team_b_id : match.team_a_id;

    const { data, error } = await supabase
      .from("matches")
      .update({
        score_verified: true,
        score_verified_at: new Date().toISOString(),
        winner_id: winnerId,
        loser_id: loserId,
        status: "completed",
        completed_at: new Date().toISOString(),
      })
      .eq("id", matchId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Captain: dispute score
  async disputeScore(matchId, reason) {
    const { data, error } = await supabase
      .from("matches")
      .update({
        score_disputed: true,
        dispute_reason: reason,
        status: "disputed",
      })
      .eq("id", matchId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Admin/Referee: resolve dispute
  async resolveDispute(matchId, finalScoreA, finalScoreB, resolvedBy) {
    const { data: match } = await supabase
      .from("matches")
      .select("team_a_id, team_b_id")
      .eq("id", matchId)
      .single();

    const winnerId =
      finalScoreA > finalScoreB ? match.team_a_id : match.team_b_id;
    const loserId =
      finalScoreA > finalScoreB ? match.team_b_id : match.team_a_id;

    const { data, error } = await supabase
      .from("matches")
      .update({
        team_a_score: finalScoreA,
        team_b_score: finalScoreB,
        winner_id: winnerId,
        loser_id: loserId,
        score_verified: true,
        score_disputed: false,
        dispute_resolved_by: resolvedBy,
        dispute_resolved_at: new Date().toISOString(),
        status: "completed",
        completed_at: new Date().toISOString(),
      })
      .eq("id", matchId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Admin: force verify (skip away captain confirmation)
  async forceVerify(matchId, adminUserId) {
    return this.resolveDispute(
      matchId,
      null, // keep existing scores
      null,
      adminUserId
    );
  },

  // Admin: assign to playing area
  async assignArea(matchId, playingAreaId) {
    const { data, error } = await supabase
      .from("matches")
      .update({
        playing_area_id: playingAreaId,
        status: "ready",
      })
      .eq("id", matchId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Admin: start match
  async startMatch(matchId) {
    // Randomize home captain
    await supabase.rpc("assign_home_captain", { match_uuid: matchId });
    const { data, error } = await supabase
      .from("matches")
      .update({
        status: "live",
        started_at: new Date().toISOString(),
      })
      .eq("id", matchId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Admin: award bye (no-show opponent)
  async awardBye(matchId, winnerTeamId, loserTeamId) {
    const { data, error } = await supabase
      .from("matches")
      .update({
        winner_id: winnerTeamId,
        loser_id: loserTeamId,
        team_a_score: 0,
        team_b_score: 0,
        score_verified: true,
        status: "completed",
        completed_at: new Date().toISOString(),
      })
      .eq("id", matchId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Admin: reassign home captain
  async reassignHomeCaptain(matchId, newHomeCaptainTeamId, newAwayCaptainTeamId) {
    const { data, error } = await supabase
      .from("matches")
      .update({
        home_captain_team_id: newHomeCaptainTeamId,
        away_captain_team_id: newAwayCaptainTeamId,
      })
      .eq("id", matchId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },
};

