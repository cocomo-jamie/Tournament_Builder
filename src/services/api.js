export const events = {
  async get(eventId) {
    const { data, error } = await supabase
      .from("events")
      .select("*, organizations(*), event_dates(*)")
      .eq("id", eventId)
      .single();
    if (error) throw error;
    return data;
  },

  async getByOrg(orgId) {
    const { data, error } = await supabase
      .from("events")
      .select("*")
      .eq("org_id", orgId)
      .order("created_at", { ascending: false });
    if (error) throw error;
    return data;
  },

  async update(eventId, updates) {
    const { data, error } = await supabase
      .from("events")
      .update(updates)
      .eq("id", eventId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  async updateFundraising(eventId, amount) {
    const { data, error } = await supabase
      .from("events")
      .update({ fundraising_current: amount })
      .eq("id", eventId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  async updateRules(eventId, rulesContent) {
    const { data, error } = await supabase
      .from("events")
      .update({ rules_content: rulesContent })
      .eq("id", eventId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  async updateStatus(eventId, status) {
    const updates = { status };
    if (status === "registration_open") updates.published_at = new Date().toISOString();
    const { data, error } = await supabase
      .from("events")
      .update(updates)
      .eq("id", eventId)
      .select()
      .single();
    if (error) throw error;
    return data;
  },
};

// ═══════════════════════════════════════════════════════════
// REGISTRATIONS
// ═══════════════════════════════════════════════════════════

export const registrations = {
  // Public: submit a new registration
  async create(eventId, formData) {
    const { data: event } = await supabase
      .from("events")
      .select("entry_fee, recon_prefix, recon_counter")
      .eq("id", eventId)
      .single();

    const { data, error } = await supabase
      .from("registrations")
      .insert({
        event_id: eventId,
        team_name: formData.teamName,
        team_slogan: formData.slogan || null,
        captain_name: formData.captainName,
        captain_email: formData.captainEmail,
        captain_phone: formData.captainPhone,
        captain_shirt: formData.captainShirt || null,
        captain_dietary: formData.captainDiet || null,
        payment_method: formData.paymentMethod,
        fee_amount: event.entry_fee,
        donation_amount: formData.donation || 0,
        total_amount: event.entry_fee + (formData.donation || 0),
        image_consent: formData.imageConsent || false,
        waiver_accepted: formData.waiverAccepted || false,
        team_story: formData.story || null,
        // reconciliation_code auto-generated by trigger
      })
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Admin: list all registrations for an event
  async list(eventId, filters = {}) {
    let query = supabase
      .from("registrations")
      .select("*")
      .eq("event_id", eventId)
      .order("created_at", { ascending: false });

    if (filters.paymentStatus) query = query.eq("payment_status", filters.paymentStatus);
    if (filters.status) query = query.eq("status", filters.status);
    if (filters.search) {
      query = query.or(
        `team_name.ilike.%${filters.search}%,captain_name.ilike.%${filters.search}%,reconciliation_code.ilike.%${filters.search}%`
      );
    }

    const { data, error } = await query;
    if (error) throw error;
    return data;
  },

  // Admin: batch update registrations (payment confirmations, rejections)
  async batchUpdate(updates) {
    // updates = [{ id, payment_status, status, payment_confirmed_by, ... }]
    const promises = updates.map(({ id, ...fields }) =>
      supabase.from("registrations").update(fields).eq("id", id)
    );
    const results = await Promise.all(promises);
    const errors = results.filter((r) => r.error);
    if (errors.length) throw new Error(`${errors.length} updates failed`);
    return results.map((r) => r.data);
  },

  // Toggle payment status independently (checkbox-driven)
  async setPaymentReceived(id, received, adminUserId) {
    const { data, error } = await supabase
      .from("registrations")
      .update({
        payment_status: received ? "paid" : "pending",
        payment_confirmed_at: received ? new Date().toISOString() : null,
        payment_confirmed_by: received ? adminUserId : null,
      })
      .eq("id", id)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Toggle registration approval independently (checkbox-driven)
  async setApproved(id, approved, adminUserId) {
    const { data, error } = await supabase
      .from("registrations")
      .update({
        status: approved ? "approved" : "submitted",
        confirmed_at: approved ? new Date().toISOString() : null,
        approved_by: approved ? adminUserId : null,
      })
      .eq("id", id)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Admin: confirm a single payment
  async confirmPayment(id, adminUserId) {
    const { data, error } = await supabase
      .from("registrations")
      .update({
        payment_status: "paid",
        status: "confirmed",
        payment_confirmed_at: new Date().toISOString(),
        payment_confirmed_by: adminUserId,
        confirmed_at: new Date().toISOString(),
      })
      .eq("id", id)
      .select()
      .single();
    if (error) throw error;
    return data;
  },

  // Admin: reject registration
  async reject(id) {
    const { data, error } = await supabase
      .from("registrations")
      .update({ status: "rejected" })
      .eq("id", id)
      .select()
      .single();
    if (error) throw error;
    return data;
  },
};

