export const pools = {
  async create(eventId, poolNames) {
    const rows = poolNames.map((name, i) => ({
      event_id: eventId,
      name,
      sort_order: i,
    }));
    const { data, error } = await supabase.from("pools").insert(rows).select();
    if (error) throw error;
    return data;
  },

  async listWithStandings(eventId) {
    const { data, error } = await supabase
      .from("pools")
      .select("*, pool_standings(*, team:teams(id, name, slogan))")
      .eq("event_id", eventId)
      .order("sort_order");
    if (error) throw error;
    // Sort standings by rank within each pool
    return data.map((pool) => ({
      ...pool,
      pool_standings: pool.pool_standings.sort((a, b) => (a.pool_rank || 99) - (b.pool_rank || 99)),
    }));
  },
};

